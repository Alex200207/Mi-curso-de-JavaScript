//para programar un ciclo necesitamos una variable contador esta variable permite terminar el ciclo en cierto momento

let contador = 0; //contador inciando en 0

while (contador < 3) {
  /*se evalua la condicion  si es verdera ejecuta el codigo y aumenta en 1 el contador
dicho contador es de post incremento es decir queda pendiente el incremento hasta que se vuelva a llamar 
una ves la condicion es falsa y el contador es menor que 3 es por ejem 2 la condicion sera falsa y terminara el ciclo */
  console.log(contador); //imprimimos cada vuelta del ciclo
  contador++; //operador unario de post incremento
}
console.log("Fin del ciclo while");//mensaje de finalizacion del ciclo



