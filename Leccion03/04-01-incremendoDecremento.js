let a = 3, b= 2;

//Incremento
//pre-incremento (el operador ++ antes de la variable)

z = ++a;//primero le sumamos el valor de 1 a la variable y luego se asigna 
console.log(a)
console.log(z)

//post-incremento (el operador  ++ despues de la variable)
z = b++;//primero asignamos la variable y despues le sumamos 1 el valor 
//queda pendiente hasta ser llamado
console.log(b)
console.log(z)

//Decremento es igual a incremento pero este disminuye el valor
//Pre-Decremento
z = --a;
console.log(a)
console.log(z)

//post-decremento
z = a--
console.log(a)
console.log(z)
